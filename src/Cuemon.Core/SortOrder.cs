namespace Cuemon
{
    /// <summary>
    /// Specifies the direction of a sort operation.
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        /// Sorts in ascending order.
        /// </summary>
        Ascending,
        /// <summary>
        /// Sorts in descending order.
        /// </summary>
        Descending,
        /// <summary>
        /// No sort order is specified, meaning the default sorting is used.
        /// </summary>
        Unspecified
    }
}